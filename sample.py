def calculate_average(num1, num2, num3):
    total = num1 + num2 + num3
    average = total / 3
    return average

# Get input from the user
num1 = input("Enter the first numbers: ")
num2 = input("Enter the second numbers: ")
num3 = input("Enter the third numbers: ")

# Convert input to integers
num1 = int(num1)
num2 = int(num2)
num3 = int(num3)

# Calculate average
result = calculate_average(num1, num2, num3)
print("The average is: ", result)

